function sumMetric(ids: string[], key: keyof Content['metrics']): number {
  return ids
    .map(id => SAMPLE_CONTENT.find(c => c.id === id))
    .filter((c): c is Content => c !== undefined && c.countsTowardStoryTotal)
    .reduce((sum, c) => sum + (c.metrics[key] as number), 0)
}

function countPlatforms(ids: string[]): number {
  const platforms = new Set(
    ids
      .map(id => SAMPLE_CONTENT.find(c => c.id === id))
      .filter((c): c is Content => c !== undefined && c.countsTowardStoryTotal)
      .map(c => c.platform)
  )
  return platforms.size
}

function buildRollup(memberIds: string[]) {
  const impressions = sumMetric(memberIds, 'impressions')
  const we = sumMetric(memberIds, 'weightedEngagement')
  return {
    impressions,
    weightedEngagement: we,
    interactions: sumMetric(memberIds, 'interactions'),
    siteClicks: sumMetric(memberIds, 'siteClicks'),
    attentionTotalMinutes: sumMetric(memberIds, 'watchReadMinutes'),
    platformCount: countPlatforms(memberIds),
    memberCount: memberIds.filter(id => {
      const c = SAMPLE_CONTENT.find(x => x.id === id)
      return c?.countsTowardStoryTotal
    }).length,
    eqr: impressions > 0 ? (we / impressions) * 100 : 0,
  }
}

